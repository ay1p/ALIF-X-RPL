public class tugas4 {
    public static void main(String[] args) {
        int nilai1 = 80;
        int nilai2 = 95;
        int nilai3 = 75;

        System.out.println("Nilai Total: ");
        nilai1 += 80;
        System.out.println("Nilai += 80=" + nilai1);
        nilai2 += 95;
        System.out.println("Nilai += 95=" + nilai2);
        nilai3 += 75;
        System.out.println("Nilai3 += 75=" + nilai3);

        System.out.println("Nilai Rata-Rata: ");
        nilai1 /= 80;
        System.out.println("Nilai1 /= 80=" + nilai1);
        nilai2 /= 95;
        System.out.println("Nilai2 /= 95=" + nilai2);
        nilai3 /= 75;
        System.out.println("Nilai3 /= 75=" + nilai3);
    }
    
}
